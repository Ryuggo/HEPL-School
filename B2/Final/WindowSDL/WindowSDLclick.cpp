#include "WindowSDLclick.h"

//***************************************************************************
//********** Constructeurs **************************************************
//***************************************************************************
WindowSDLclick::WindowSDLclick(void)
{
  setX(0);
  setY(0);
}

WindowSDLclick::WindowSDLclick(int x,int y)
{
  setX(x);
  setY(y);
}

WindowSDLclick::WindowSDLclick(const WindowSDLclick &c)
{
  setX(c.getX());
  setY(c.getY());
}

//***************************************************************************
//********** Desctructeur ***************************************************
//***************************************************************************
WindowSDLclick::~WindowSDLclick() { }

//***************************************************************************
//********** SETTERS ********************************************************
//***************************************************************************
void WindowSDLclick::setX(int x)
{
  X = x;
}

void WindowSDLclick::setY(int y)
{
  Y = y;
}

//***************************************************************************
//********** GETTERS ********************************************************
//***************************************************************************
int WindowSDLclick::getX() const { return X; }
int WindowSDLclick::getY() const { return Y; }

